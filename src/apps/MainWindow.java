    private void jButton40ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton40ActionPerformed
        // Code for Add Product
        try {
            String pname = jComboBox3.getSelectedItem().toString();
            JOptionPane.showMessageDialog(this,pname);
            String cmp = jTextField46.getText();
            String ptype = jTextField47.getText();
            String price = jTextField48.getText();
            int qty = Integer.parseInt(jTextField49.getText());
            int contact=Integer.parseInt(jTextField50.getText());
            DBCon db = new DBCon();
            db.pstmt = db.con.prepareStatement("SELECT p_qty FROM stock WHERE p_name=?");
            db.pstmt.setString(1, pname);
            db.rst = db.pstmt.executeQuery();
            db.rst.next();
            int avl_qty = db.rst.getInt(1);
            if (avl_qty >= qty) {
                db.pstmt = db.con.prepareStatement("UPDATE stock SET p_qty=? WHERE p_name=?");
                db.pstmt.setInt(1, (avl_qty - qty));
                db.pstmt.setString(2, pname);
                int i1 = db.pstmt.executeUpdate();
                db.pstmt = db.con.prepareStatement("INSERT INTO invice(invc_no,p_name,p_company,p_qty,p_sp,cust_cont,date_time) values(?,?,?,?,?,?,?)");
                db.pstmt.setInt(1, invc);
                db.pstmt.setString(2, pname);
                db.pstmt.setString(3, cmp);
                db.pstmt.setInt(4, qty);
                db.pstmt.setString(5, price);
                db.pstmt.setInt(6, contact);
                db.pstmt.setString(7, (new java.util.Date()).toString());
                int i2 = db.pstmt.executeUpdate();
                if (i1 > 0 && i2 > 0) {
                    JOptionPane.showMessageDialog(this, "Product Added in Cart Successfully");
                    jComboBox3.setSelectedIndex(0);
                    jTextField46.setText("");
                    jTextField47.setText("");
                    jTextField48.setText("");
                    jTextField49.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Quantity of Product");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton40ActionPerformed

